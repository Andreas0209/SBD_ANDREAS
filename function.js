function bio(name, age, address, hobby) {
  return `Nama saya ${name}, umur saya ${age} tahun, alamat saya di ${address}, dan saya punya hobi yaitu ${hobby}!`;
}

// Contoh pemanggilan fungsi dengan parameter
const perkenalan = bio("John Doe", 25, "Jl. Contoh No. 123", "membaca buku");
console.log(perkenalan);